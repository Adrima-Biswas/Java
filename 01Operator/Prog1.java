class Main
{
	public static void main(String[] args) {
	   { 
		   // Relational Operator : < , <= , >= , > , == , !=
	       boolean b1 = 4 < 1;

	       boolean b2 = (4 > 4);

	       boolean b = (4 != 4);

	       System.out.print(b);
	   } 
	   
	   { 
		   // Logical Operator : && , || , !
	       boolean b1 = (4 > 7) && (6 != 0);
	       boolean b2 = (4 > 7) || (6 != 6);
	       boolean b = !(4 > 7);
	       System.out.print(b); 
	   }
	}
}  
